package com.main.java2;

public class TriAngle {
	private double base;
	private double height;
	
	public TriAngle() {
		
	}
public TriAngle(double b,double h) {
		base=b;
		height=h;
	}
	public  void setbase(double b) {
		base=b;
	}
	public double getbase() {
		return base;
	}
	public void setheight(double h) {
		height=h;
	}
	public double getheight() {
		return height;
	}
	
	
}
